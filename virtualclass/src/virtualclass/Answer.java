package virtualclass;

public class Answer {
	private String answer;
	private int id;
	private String name;
	private long time;

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}

	@Override
	public String toString() {
		return "Answer [answer=" + answer + ", id=" + id + ", name=" + name + ", time=" + time + "]";
	}
}
